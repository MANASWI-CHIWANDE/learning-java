
/*
Write a Java Program to assign byte, int, long, float,double, char,
string value to variable and also display the same value on screen.
 */
public class LAB_1_b {
    public static void main(String[] args) {
        byte a = 111;
        int b = 67;
        long c = 34L;
        float x = 23.456f;
        double y = 23.456d;
        char d = 'A';
        String s = "MANASWI";
        System.out.println("BYTE VALUE OF VARIABLE IS : "+a);
        System.out.println("INT VALUE OF VARIABLE IS : "+b);
        System.out.println("LONG VALUE OF VARIABLE IS : "+c);
        System.out.println("FLOAT VALUE OF VARIABLE IS : "+x);
        System.out.println("DOUBLE VALUE OF VARIABLE IS : "+y);
        System.out.println("CHAR VALUE OF VARIABLE IS : "+d);
        System.out.println("STRING VALUE OF VARIABLE IS : "+s);
    }
}
